package model;

public class Login {
	private String accNo;
	private String pass;
	
	
	public void setAccNo(String accNo)
	{
		this.accNo=accNo;
	}
	public void setPass(String pass)
	{
		this.pass=pass;
	}
	
	public String getAccNo()
	{
		return(accNo);
	}
	public String getPass()
	{
		return(pass);
	}

}
